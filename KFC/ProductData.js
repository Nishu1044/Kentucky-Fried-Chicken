let container = document.querySelector("#container");

let inputbox = document.querySelector("div>input");
let searchBtn = document.querySelector("#seaBtn");


/local storage part/ 
let LSData = localStorage.getItem("cart");
if(LSData === null){
    LSData = [];
}else{
    LSData = JSON.parse(LSData);
}


function ShowData(arr){
    container.innerHTML=""
     
    arr.forEach(function(ele,i){

        let separateDiv = document.createElement("div")
        separateDiv.classList.add("product");
        
         let image = document.createElement("img")
         image.src = ele.image
        
          let Title = document.createElement("h3")
          Title.innerHTML = ele.title
        
          let Name = document.createElement("h4")
          Name.innerHTML = ele.name
        
        
          let Type = document.createElement("p")
          Type.innerHTML = ele.type
        
        
          let Price = document.createElement("h5")
          Price.innerHTML = ele.price
        
          let Des = document.createElement("p")
          Des.innerHTML = ele.description
        
          let btn = document.createElement("Button")
          btn.innerText = "Add"

          btn.addEventListener("click", function() {
            AddToCart(ele); // Passing product info to AddToCart function
        });
         
        
          separateDiv.append(image,Title,Name,Type,Price,Des,btn)
          container.append(separateDiv)
         });
}

ShowData(ProData);


 function searchData(){
   let value = inputbox.value;
  
   let newarr = ProData.filter(function(ele,i){
     return ele.title.toLowerCase() === value.toLowerCase() || ele.name.toLowerCase() === value.toLowerCase();
    
   })
//    console.log(newarr);

if(newarr.length>0){
    ShowData(newarr)
}else{
    container.innerHTML = "<h2> Product is not found ..... <h2/>"
}

 }

 searchBtn.addEventListener("click", searchData)

